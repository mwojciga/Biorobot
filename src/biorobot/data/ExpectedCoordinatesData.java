package biorobot.data;

/**
 * Expected coordinates DTO.
 * @author maciej.wojciga
 *
 */
public class ExpectedCoordinatesData {
	private int expectedX;
	private int expectedY;
	private int expectedZ;
	
	public int getExpectedX() {
		return expectedX;
	}
	public void setExpectedX(int expectedX) {
		this.expectedX = expectedX;
	}
	public int getExpectedY() {
		return expectedY;
	}
	public void setExpectedY(int expectedY) {
		this.expectedY = expectedY;
	}
	public int getExpectedZ() {
		return expectedZ;
	}
	public void setExpectedZ(int expectedZ) {
		this.expectedZ = expectedZ;
	}
	
}
